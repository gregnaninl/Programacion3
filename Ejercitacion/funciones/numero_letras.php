function NumerosaLetras($num)
{
$valor;

switch($num)
{

    case 20 :
    $valor= "Veinte";
    break;
    case 21 :
    $valor= "Veintiuno";
    break;
    case 22 :
    $valor= "Veintidos";
    break;
    case 23 :
    $valor= "Veintitres";
    break;
    case 24 :
    $valor= "Veinticuatro";
    break;
    case 25 :
    $valor= "Veinticinco";
    break;
    case 26 :
    $valor= "Veintiseis";
    break;
    case 27 :
    $valor= "Veintisiete";
    break;
    case 28 :
    $valor= "Veintiocho";
    break;
    case 29 :
    $valor= "Veintinueve";
    break;
    case 30 :
    $valor= "Treinta";
    break;
    case 31:
    $valor= "Treinta y";
    break;
    case 32:
    $valor= "Treinta y dos";
    break;
    case 33:
    $valor= "Treinta y tres";
    break;
    case 34:
    $valor= "Treinta y cuatro";
    break;
    case 35:
    $valor= "Treinta y cinto";
    break;
    case 36:
    $valor= "Treinta y seis";
    break;
    case 37:
    $valor= "Treinta y siete";
    break;
    case 38:
    $valor= "Treinta y ocho";
    break;
    case 39:
    $valor= "Treinta y nueve";
    break;
    case 40:
    $valor= "cuarenta";
    break;
    default:
    $valor = "El numero no esta adentro del rango";

}

return $valor;

}




